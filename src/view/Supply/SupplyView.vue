import DaumPost from "@/components/Daum/DaumPost";
import '@toast-ui/editor/dist/toastui-editor.css';
import { Editor } from '@toast-ui/vue-editor';

export default {
	name: 'SupplyDetail'
	, components: {DaumPost, Editor}
	, props: ['Axios', 'user', 'codes', 'rules', 'date', 'TOKEN']
	, data: function(){
		return {
			program: {
				name: '상점 설정'
				, top: true
				, title: true
				, bottom: false
			}
			,item: {
				supply_type: ''
				,business_type: 'B002001'
				,bank_code: ''
				,join_date: this.date.getToday('-')
				,delivery_type: '0'
				,shop_return: ''
			}
			,is_data_pick: false
			,is_modal: false
			,is_post: false
			,daum_config: {
				width: '360px'
			}
		}
	}
	,computed: {

	}
	, methods: {
		getData: async function(){
			try{
				this.$emit('onLoading')
				const result = await this.Axios({
					method: 'get'
					,url: 'management/getSupply'
					,data: {
						supply_uid: this.user.uid
					}
				})
				if(result.success){
					this.item = result.data
					this.item.ATOKEN = this.TOKEN
					this.item.UTOKEN = this.TOKEN
				}else{
					this.$emit('setNotify', { type: 'error', message: result.message})
				}
			}catch(e){
				console.log(e)
			}finally {
				this.$emit('offLoading')
			}
		}
		, save: async function(){
			try{

				this.$emit('onLoading')

				let shop_return = this.$refs.shop_return.invoke("getMarkdown")

				if(!shop_return){
					this.$refs.shop_return.invoke("setMarkdown", this.item.shop_return)
				}

				this.item.shop_return = shop_return

				const result = await this.Axios({
					method: 'post'
					,url: 'management/putSupply'
					,data: this.item
				})
				if(result.success){
					this.$emit('setNotify', { type: 'success', message: result.message})
					await this.getData()
				}else{
					this.$emit('setNotify', { type: 'error', message: result.message})
				}
			}catch(e){
				console.log(e)
			}finally {
				this.$emit('offLoading')
			}
		}
		,toList: function(){
			this.$storage.push({ name: 'SupplyList'})
		}

		,daumPost: function () {
			this.is_post = true
		}
		, addPost: function (call) {

			this.$set(this.item, 'shop_zip_code', call.zonecode)
			this.$set(this.item, 'shop_address', call.address)

			this.is_post = false
		}
	}
	, created() {
		this.$emit('onLoad', this.program)
		this.getData()
	}
}
</script>


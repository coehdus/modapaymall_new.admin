<template>
	<div class="full-height">
		<div
			class="full-height"
		>
			<div
				class="justify-space-between "
			>
				<div class="flex-1 bg-white pa-10 mr-10">
					<h6 class="under-line-identify">계정정보</h6>
					<table class="table th-left td-left">
						<col width="120px">
						<col width="auto">
						<col width="120px">
						<col width="auto">
						<tbody>
							<tr>
								<th>아이디 <span class="color-red">*</span></th>
								<td>
									<input
										v-model="item.admin_id"
										class="input-box full-width pa-5-10 mr-10"
										placeholder="아이디를 입력하세요"
										:rules="[rules.id(item, 'admin_id', {min: 4, max: 25 })]"

										@keyup.enter="save"
									/>
								</td>
								<th>등급</th>
								<td class="position-relative">
									<select
										v-model="item.admin_level"
										class="input-box position-relative cursor-pointer"
										style="z-index: 1"
									>
										<option value="">선택하세요</option>
										<option
											v-for="(code, index) in codes.A002.items"
											:key="code.total_code + index"
											:value="code.code_value"
										>{{ code.code_name }}</option>
									</select>
									<v-icon
										class="position-absolute color-icon"
										style="right: 15px; top: 11px; z-index: 0"
									>mdi mdi-menu-down</v-icon>
								</td>
							</tr>
							<tr>
								<th>비밀번호 <span class="color-red">*</span></th>
								<td class="">
									<input
										v-model="item.admin_password"
										type="password"
										class="input-box full-width"
										placeholder="비밀번호"
										max="50"

										@keyup.enter="save"
									/>
								</td>

								<th>비밀번호 확인 <span class="color-red">*</span></th>
								<td class="">
									<input
										v-model="item.admin_password_confirm"
										type="password"
										class="input-box full-width"
										placeholder="비밀번호를 한번 더 입력하세요"
										max="50"

										@keyup.enter="save"
									/>
								</td>
							</tr>
							<tr>
								<th>이름 <span class="color-red">*</span></th>
								<td>
									<input
										type="text"
										v-model="item.admin_name"
										class="input-box full-width"
										placeholder="이름을 입력하세요"
										maxlength="20"

										@keyup.enter="save"
									/>
								</td>
								<th>휴대폰 번호</th>
								<td>
									<input
										type="number"
										v-model="item.admin_phone"
										:ruels="[rules.max(item, 'admin_phone', 12)]"
										class="input-box full-width"
										placeholder="휴대폰번호를 입력하세요"

										@keyup.enter="save"
									/>
								</td>
							</tr>
						</tbody>
					</table>
				</div>
				<div class="flex-1"></div>
			</div>
		</div>

		<div
			class="justify-center"
		>
			<button
				class="bg-identify pa-10-20 mr-10"
				@click="save"
			>저장</button>
			<button
				class="box pa-10-20"
				@click="toList"
			>목록</button>
		</div>
		<DaumPost
			:overlay="is_post"
			:config="daum_config"
			@callBack="addPost"
		></DaumPost>
	</div>
</template>

<script>

import DaumPost from "@/components/Daum/DaumPost";
export default {
	name: 'ManagerItem'
	,
	components: {DaumPost},
	props: ['Axios', 'user', 'codes', 'rules']
	,data: function(){
		return {
			program: {
				name: '관리자 등록'
				, top: true
				, title: true
				, bottom: false
			}
			,item: {
				admin_type: 'A001001'
				, admin_level: ''
			}
			,sample: {

			}
			,is_modal: false
			,modal_option: {
				title: '비밀번호 초기화'
				,content: '비밀번호를 초기화 하고 등록된 연락처로 전송하시겠습니까?'
				,top: true
				,bottom: true
				,width: '360px'
			}
			,is_modal2: false
			,modal_option2: {
				title: '영업점 계정상태 변경'
				,top: true
				,bottom: true
				,width: '360px'
			}
			,is_modal3: false
			,modal_option3: {
				title: '첨부파일 삭제'
				,top: true
				,bottom: true
				,content: '해당 첨부파일을 삭제하시겠습니까?'
				,width: '360px'
			}
			,is_post: false
			,daum_config: {
				width: '360px'
			}
		}
	}
	,computed: {

	}
	, methods: {
		save: async function(){
			try{
				this.$emit('onLoading')
				const result = await this.Axios({
					method: 'post'
					,url: 'management/postAdmin'
					,data: this.item
				})
				if(result.success){
					this.$emit('setNotify', { type: 'success', message: result.message})
					this.toList()
				}else{
					this.$emit('setNotify', { type: 'error', message: result.message})
				}
			}catch(e){
				console.log(e)
			}finally {
				this.$emit('offLoading')
			}
		}
		,isPassword: function(){
			this.clear()
			this.is_modal = true
		}
		,isStatus: function(){
			this.clear()
			this.is_modal2 = true
		}
		,clear: function(){
			this.is_modal = false
			this.is_modal2 = false
			this.is_modal3 = false
		}
		,toList: function(){
			this.$router.back()
		}

		,daumPost: function () {
			this.is_post = true
		}
		, addPost: function (call) {

			this.$set(this.item, 'shop_post', call.zonecode)
			this.$set(this.item, 'shop_addr1', call.address)

			this.is_post = false
		}
	}
	, created() {
		this.$emit('onLoad', this.program)
	}
}
</script>

